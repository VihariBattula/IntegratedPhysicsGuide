const coverKicker = document.getElementById("coverKicker");
const coverTitle = document.getElementById("coverTitle");
const coverSubtitle = document.getElementById("coverSubtitle");
const detailTitle = document.getElementById("detailTitle");
const detailDescription = document.getElementById("detailDescription");
const detailPoints = document.getElementById("detailPoints");
const topicButtons = Array.from(document.querySelectorAll("[data-topic]"));
const groupToggles = Array.from(document.querySelectorAll(".group-toggle"));
const unitToggles = Array.from(document.querySelectorAll(".unit-toggle"));

const menuToggle = document.getElementById("menuToggle");
const sidebar = document.getElementById("sidebar");
const sidebarBackdrop = document.getElementById("sidebarBackdrop");
const printButton = document.getElementById("printButton");
const lastUpdated = document.getElementById("lastUpdated");

function formatDateISO(date) {
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, "0");
  const day = String(date.getDate()).padStart(2, "0");
  return `${year}-${month}-${day}`;
}

function renderTopic(topicId) {
  const selected = topicContent[topicId] || topicContent.cover;

  coverKicker.textContent = selected.kicker;
  coverTitle.textContent = selected.title;
  coverSubtitle.textContent = selected.subtitle;
  detailTitle.textContent = selected.detailTitle;
  detailDescription.textContent = selected.detailDescription;

  detailPoints.innerHTML = "";
  selected.points.forEach((point) => {
    const li = document.createElement("li");
    li.textContent = point;
    detailPoints.appendChild(li);
  });
}

function clearActiveStates() {
  topicButtons.forEach((button) => button.classList.remove("is-active"));
}

function setActiveTopic(button) {
  clearActiveStates();
  button.classList.add("is-active");

  const unitGroup = button.closest(".unit-group");
  const navGroup = button.closest(".nav-group");

  if (button.classList.contains("sub-item") && unitGroup) {
    unitGroup.classList.add("is-open");
  }

  if (navGroup) {
    navGroup.classList.add("is-open");
  }
}

function setSidebarOpen(open) {
  if (!sidebar || !menuToggle || !sidebarBackdrop) {
    return;
  }

  sidebar.classList.toggle("open", open);
  sidebarBackdrop.classList.toggle("visible", open);
  menuToggle.setAttribute("aria-expanded", String(open));
}

function closeSidebarOnMobile() {
  if (window.innerWidth <= 1040) {
    setSidebarOpen(false);
  }
}

if (lastUpdated) {
  lastUpdated.textContent = formatDateISO(new Date());
}

groupToggles.forEach((toggle) => {
  toggle.addEventListener("click", () => {
    const group = toggle.closest(".nav-group");
    if (group) {
      group.classList.toggle("is-open");
    }
  });
});

unitToggles.forEach((toggle) => {
  toggle.addEventListener("click", () => {
    const group = toggle.closest(".unit-group");
    if (group) {
      group.classList.toggle("is-open");
    }
  });
});

topicButtons.forEach((button) => {
  button.addEventListener("click", () => {
    const topicId = button.dataset.topic;
    if (!topicId) {
      return;
    }

    setActiveTopic(button);
    renderTopic(topicId);
    closeSidebarOnMobile();
  });
});

if (menuToggle) {
  menuToggle.addEventListener("click", () => {
    const shouldOpen = !sidebar.classList.contains("open");
    setSidebarOpen(shouldOpen);
  });
}

if (sidebarBackdrop) {
  sidebarBackdrop.addEventListener("click", () => {
    setSidebarOpen(false);
  });
}

if (printButton) {
  printButton.addEventListener("click", () => {
    window.print();
  });
}

window.addEventListener("resize", () => {
  if (window.innerWidth > 1040) {
    setSidebarOpen(false);
  }
});

const defaultTopicButton = document.querySelector('[data-topic="cover"]');
if (defaultTopicButton) {
  setActiveTopic(defaultTopicButton);
  renderTopic(defaultTopicButton.dataset.topic);
}
